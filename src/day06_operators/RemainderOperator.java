package day06_operators;

public class RemainderOperator {
    public static void main(String[] args){
        System.out.println(10/3);//3
        System.out.println(10%3);//1(remainder yani kalan)

        System.out.println(20%5);//0 because no remainder//5+5+5+5
        System.out.println(19%5);//4//5+5+5+4
        System.out.println(2%5);//2 if the first number smaller than second number, then remainder is the value of first number.
        //Because whole number is already remainder

    }
}
