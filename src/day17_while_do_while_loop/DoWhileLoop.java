package day17_while_do_while_loop;

public class DoWhileLoop {
    public static void main(String[] args) {
        int n = 0;
        do {
            System.out.println(n);
            n++;
        } while (n <= 10);
        System.out.println();

        int n2 = 0;
        do {
            System.out.println(n2 +"");
            n2 += 3;
        } while (n2 <= 100);

        System.out.println();
        int dortler=0;
        do{
            System.out.println(dortler+"");
            dortler+=4;
        }while (dortler<=40);
    }
    }

